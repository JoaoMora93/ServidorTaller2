    private void mostrarPuntajeTotal(int puntajePregunta) {
    
        int puntTotal = 0;
      
       // int[] puntajePreg  = new int[CANTPREG]; 
               
        
       
        
        if (this.puntajePregunta1.getText().length() == 0) 
            this.puntajePreg[0] = 0;
        else
            this.puntajePreg[0] = Integer.parseInt(this.puntajePregunta1.getText()); 
        
      
        if (this.puntajePregunta2.getText().length() == 0) 
            this.puntajePreg[1] = 0;
        else
            this.puntajePreg[1] = Integer.parseInt(this.puntajePregunta2.getText()); 
        
        if (this.puntajePregunta3.getText().length() == 0) 
            this.puntajePreg[2] = 0;
        else
            this.puntajePreg[2] = Integer.parseInt(this.puntajePregunta3.getText()); 
        
        
        if (this.puntajePregunta4.getText().length() == 0) 
            this.puntajePreg[3] = 0;
        else
            this.puntajePreg[3] = Integer.parseInt(this.puntajePregunta4.getText()); 
        
    
        if (this.puntajePregunta5.getText().length() == 0) 
            this.puntajePreg[4] = 0;
        else
            this.puntajePreg[4] = Integer.parseInt(this.puntajePregunta5.getText()); 
        
      
        if (this.puntajePregunta6.getText().length() == 0) 
            this.puntajePreg[5] = 0;
        else
            this.puntajePreg[5] = Integer.parseInt(this.puntajePregunta6.getText()); 
            
      
      
        
        for (int i=0; i < CANTPREG; i++)
            puntTotal = puntTotal + this.puntajePreg[i];
        
        this.puntajeTotal.setText(Integer.toString(puntTotal));// mostrar  puntaje total
    }
    
