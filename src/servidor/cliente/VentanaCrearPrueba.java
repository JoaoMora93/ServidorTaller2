    private void mostrarPreguntasDocente() {
    
        AccesoPersistencia accesoPersistencia = AccesoPersistencia.getInstancia();    
        List <String> preguntas =  accesoPersistencia.devolverPreguntasDocente(5000000); 
       
        agregarFilasTabla(this.tablaPreguntasDocente,0);
        agregarFilasTabla(this.tablaPreguntasDocente,preguntas.size());
       
      //  this.tablaPreguntasDocente.setValueAt("Programacion",0,0);
       // this.tablaPreguntasDocente.setValueAt("Multiple Opcion",0,1);
        
        Iterator it = preguntas.iterator();
        int fila = 0;
        while(it.hasNext()) {
            String  pregunta = (String) it.next(); //Aqui tomamos la clase del cliente
            this.tablaPreguntasDocente.setValueAt(pregunta,fila,0);
            this.tablaPreguntasDocente.setValueAt("Multiple Opcion",fila,1);
            fila++;
        }
    }    
    
